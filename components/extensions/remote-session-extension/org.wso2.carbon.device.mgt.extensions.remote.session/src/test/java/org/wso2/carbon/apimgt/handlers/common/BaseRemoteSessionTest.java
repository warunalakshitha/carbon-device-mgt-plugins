package org.wso2.carbon.apimgt.handlers.common;

import org.testng.annotations.BeforeSuite;
import org.wso2.carbon.base.MultitenantConstants;
import org.wso2.carbon.context.PrivilegedCarbonContext;
import org.wso2.carbon.device.mgt.common.DeviceManagementException;
import org.wso2.carbon.device.mgt.common.InvalidDeviceException;
import org.wso2.carbon.device.mgt.common.authorization.DeviceAccessAuthorizationException;
import org.wso2.carbon.device.mgt.common.operation.mgt.OperationManagementException;

import java.io.File;
import java.io.IOException;

/**
 * This is the base test case for Remote Session Management Service tests.
 */
public class BaseRemoteSessionTest {

    @BeforeSuite
    public void init() throws DeviceAccessAuthorizationException, OperationManagementException, InvalidDeviceException,
            IOException, DeviceManagementException {
        setUpCarbonHome();
    }

    private void setUpCarbonHome() {
        if (System.getProperty("carbon.home") == null) {
            File file = new File("src/test/resources/carbon-home");
            if (file.exists()) {
                System.setProperty("carbon.home", file.getAbsolutePath());
            }
        }
        PrivilegedCarbonContext.getThreadLocalCarbonContext().setTenantDomain(MultitenantConstants
                .SUPER_TENANT_DOMAIN_NAME);
        PrivilegedCarbonContext.getThreadLocalCarbonContext().setTenantId(MultitenantConstants.SUPER_TENANT_ID);
    }

}
